def populate_legal_content(apps, schema_editor):
    Site = apps.get_model("wagtailcore", "Site")
    LegalSettings = apps.get_model("core", "LegalSettings")

    site = Site.objects.filter(is_default_site=True).first()
    if site is None:
        return

    obj, _ = LegalSettings.objects.get_or_create(site=site)
    if not obj.privacy_policy:
        obj.privacy_policy = PRIVACY_POLICY_HTML
    if not obj.collection_notice:
        obj.collection_notice = COLLECTION_NOTICE_HTML
    obj.save()


